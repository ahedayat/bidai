"""Persian prompt templates for RAG answer generation (Phase 4)."""

from __future__ import annotations

from langchain_core.prompts import ChatPromptTemplate

RAG_SYSTEM_PROMPT = """\
شما یک دستیار متخصص در پاسخ‌گویی به پرسش‌های مربوط به اسناد مناقصه و تأمین کالا و خدمات هستید.

قوانین پاسخ‌گویی:
1. فقط و فقط بر اساس «متن‌های بازیابی‌شده» ارائه‌شده در پیام کاربر پاسخ دهید.
2. پاسخ را به زبان فارسی، روان، دقیق و در حد لازم بنویسید.
3. اگر پاسخ در متن‌های بازیابی‌شده وجود ندارد، صریحاً بگویید:
   «پاسخ این پرسش در سند مناقصه ارائه‌شده یافت نشد.»
4. هرگز تاریخ، عدد، مهلت، قیمت، الزامات یا شرایط حقوقی/مناقصه‌ای را حدس نزنید یا از خودتان اضافه نکنید.
5. هر جا امکان دارد، شماره صفحه منبع را در پاسخ ذکر کنید (مثلاً: «طبق صفحه ۳»).
6. اگر اطلاعات ناقص است، فقط همان بخشی را که در متن آمده است بیان کنید و از تکمیل حدسی خودداری کنید.
7. پاسخ را مختصر اما کامل نگه دارید؛ از تکرار غیرضروری متن منبع خودداری کنید.
"""

RAG_USER_PROMPT = """\
متن‌های بازیابی‌شده از سند مناقصه:

{context}

---

پرسش کاربر:
{question}

لطفاً بر اساس متن‌های بالا به پرسش پاسخ دهید. اگر پاسخ در متن‌ها نیست، صریحاً اعلام کنید که در سند یافت نشد.
"""

RAG_PROMPT = ChatPromptTemplate.from_messages(
    [
        ("system", RAG_SYSTEM_PROMPT),
        ("human", RAG_USER_PROMPT),
    ]
)
